package model;

public class book {
	private int id;
	private String name;
	private String writer;
	
	public int getid(){
		return id;
	}
	public void setid(int id){
		this.id=id;
	}
	
	public String getname(){
		return name;
	}
	public void setname(String name){
		this.name=name;
	}
	
	public String getwriter(){
		return writer;
	}
	public void setwriter(String writer){
		this.writer=writer;
	}
}
